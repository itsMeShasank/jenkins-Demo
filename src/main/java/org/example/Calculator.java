package org.example;


public class Calculator {

    public int addTwoNumbers(int num1,int num2) {

        return num1+num2;
    }
    public int subtractTwoNumbers(int num1,int num2) {

        return num1-num2;
    }

    public int multiplyTwoNumbers(int num1,int num2) {
        return num1 * num2;
    }
}
